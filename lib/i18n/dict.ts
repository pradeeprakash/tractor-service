// Recursive key-path: turns the nested dict into "nav.home" | "customer.add" | …
type Join<K, P> = K extends string
  ? P extends string
    ? `${K}.${P}`
    : never
  : never;
type Leaves<T> = T extends string
  ? ""
  : {
      [K in keyof T]: Join<K, Leaves<T[K]>>;
    }[keyof T];
type StripTrailing<S extends string> = S extends `${infer X}.` ? X : S;
export type TKey = StripTrailing<Leaves<Dict>>;

